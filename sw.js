// Service Worker for Deepan Alve Portfolio
// Version 1.0

const CACHE_NAME = 'deepanalve-portfolio-v1';
const urlsToCache = [
  '/',
  '/index.html',
  '/asset/css/styles.css',
  '/asset/css/bootstrap.css',
  '/asset/css/animate.min.css',
  '/asset/js/main.js',
  '/asset/js/jquery.min.js',
  '/asset/js/gsap.min.js',
  '/asset/font/fonts.css',
  '/asset/images/logo/logo-1.svg',
  '/asset/images/avatar/avatar-full.png',
  '/manifest.json'
];

// Install Service Worker
self.addEventListener('install', event => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then(cache => {
        console.log('Opened cache');
        return cache.addAll(urlsToCache);
      })
  );
});

// Cache and return requests
self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request)
      .then(response => {
        // Cache hit - return response
        if (response) {
          return response;
        }
        return fetch(event.request).then(
          response => {
            // Check if valid response
            if (!response || response.status !== 200 || response.type !== 'basic') {
              return response;
            }

            // Clone the response
            const responseToCache = response.clone();

            caches.open(CACHE_NAME)
              .then(cache => {
                cache.put(event.request, responseToCache);
              });

            return response;
          }
        );
      })
  );
});

// Update Service Worker
self.addEventListener('activate', event => {
  const cacheWhitelist = [CACHE_NAME];
  event.waitUntil(
    caches.keys().then(cacheNames => {
      return Promise.all(
        cacheNames.map(cacheName => {
          if (cacheWhitelist.indexOf(cacheName) === -1) {
            return caches.delete(cacheName);
          }
        })
      );
    })
  );
});
